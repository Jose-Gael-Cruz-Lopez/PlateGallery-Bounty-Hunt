function parsePlatesQuery(path: string) {
  const url = new URL(path, "http://mock");
  const sort = url.searchParams.get("sort") ?? "newest";
  const state = url.searchParams.get("state") ?? null;
  return { sort, state };
}

export function getMockResponse<T>(path: string): T {
  // Single-plate list (used by usePlate to find by id)
  if (path === "/api/plates") {
    return MOCK_PLATES as unknown as T;
  }

  // Paginated plate feed
  if (path.startsWith("/api/plates?")) {
    const { sort, state } = parsePlatesQuery(path);
    let plates = state
      ? MOCK_PLATES.filter((p) => p.state === state)
      : [...MOCK_PLATES];

    if (sort === "top") {
      plates.sort((a, b) => b.netScore - a.netScore);
    } else if (sort === "trending") {
      plates.sort((a, b) => b.hotScore - a.hotScore);
    } else {
      plates.sort(
        (a, b) => new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime()
      );
    }

    const page: PlatePage<Plate> = { items: plates, nextCursor: null };
    return page as unknown as T;
  }

  // Leaderboard
  if (path.startsWith("/api/leaderboard")) {
    const url = new URL(path, "http://mock");
    const state = url.searchParams.get("state") ?? null;
    const plates = state
      ? MOCK_PLATES.filter((p) => p.state === state)
      : [...MOCK_PLATES];
    const sorted = plates.sort((a, b) => b.netScore - a.netScore);
    return sorted as unknown as T;
  }

  // State stats
  if (path === "/api/states") {
    return MOCK_STATE_STATS as unknown as T;
  }

  throw new Error(`[mock] No handler for path: ${path}`);
}
